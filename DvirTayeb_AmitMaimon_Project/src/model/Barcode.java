package model;

public class Barcode {
	private String barcode;

	public Barcode(String barcode) {
		this.barcode = barcode;
	}
	
	public void setBarcode(String barcode) {
		this.barcode = barcode;
	}
	
	public String getBarcode() {
		return barcode;
	}
	
	public String toString() {
		return barcode;
	}
}
